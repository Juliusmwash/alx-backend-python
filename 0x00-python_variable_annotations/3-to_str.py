#!/usr/bin/env python3
"""
This module defines the type-annotated function 'to_str'.
"""


def to_str(n: float) -> str:
    """
    A type-annotated function to_str that takes a float as an argument and
    returns the string representation of the float.
    Args:
        n(float) - type-annotated argument.
    """
    return str(n)
